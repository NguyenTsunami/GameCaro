    /**
     * Creates new form BackgroundPane
     */
    private Image img;

    public ImagePane(Image img) {
        initComponents();
        this.img = img;
        Dimension size = new Dimension(img.getWidth(null), img.getHeight(null));
        setPreferredSize(size);
        setMinimumSize(size);
        setMaximumSize(size);
        setSize(size);
        setLayout(null);
    }

    @Override
    public void paintComponent(Graphics g) {
        setOpaque(false);
        g.drawImage(img, (this.getWidth() - this.getHeight())/2, 0,
                this.getHeight(), this.getHeight(), null);
    }

