    public Player getDataPlayer() {
        Player p = new Player();
        p.setName(txtName.getText());
        p.setAcc(txtAcc.getText());
        p.setPass(txtPass.getText());
        if (radAva1.isSelected()) {
            p.setAva("images/ava1.png");
        } else {
            p.setAva("images/ava2.png");
        }
        p.setScore(0);
        return p;
    }

