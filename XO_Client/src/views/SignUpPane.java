    @Override
    public void keyReleased(KeyEvent e) {
        boolean validData = true;

        //check name empty
        if (txtName.getText().isEmpty()) {
            lblNameReport.setText("Name can not empty!");
            lblNameReport.setForeground(Color.yellow);
            validData = validData & false;
        } else {
            lblNameReport.setText("Valid!");
            lblNameReport.setForeground(Color.green);
            validData = validData & true;
        }

        //check pass empty
        if (txtPass.getText().isEmpty()) {
            lblPassReport.setText("Password can not empty!");
            lblPassReport.setForeground(Color.yellow);
            validData = validData & false;
        } else {
            lblPassReport.setText("Valid!");
            lblPassReport.setForeground(Color.green);
            validData = validData & true;
        }

        //check acc existed/empty
        try {
            if (txtAcc.getText().isEmpty()) {
                lblAccReport.setText("Account can not empty!");
                lblAccReport.setForeground(Color.yellow);
                validData = validData & false;
            } else if (mainScreen.requestStub.isExist(txtAcc.getText())) {
                lblAccReport.setText("Account already exist!");
                lblAccReport.setForeground(Color.red);
                validData = validData & false;
            } else {
                lblAccReport.setText("Valid!");
                lblAccReport.setForeground(Color.green);
                validData = validData & true;
            }
        } catch (RemoteException ex) {
            Logger.getLogger(SignUpPane.class.getName()).log(Level.SEVERE, null, ex);
        }

        //set enable button oke
        if (validData) {
            btnOke.setEnabled(true);
        } else {
            btnOke.setEnabled(false);
        }

    }
